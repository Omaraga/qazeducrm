    /**
     * {@inheritdoc}
     */
    public function run()
    {
        return self::show($this->name, $this->size, $this->class, $this->options);
    }

    /**
     * Рендер иконки (статический метод)
     *
     * @param string $name имя иконки
     * @param string $size размер (xs, sm, md, lg, xl)
     * @param string $extraClass дополнительные классы
     * @param array $options HTML опции
     * @return string
     */
    public static function show(string $name, string $size = 'md', string $extraClass = '', array $options = []): string
    {
        $sizeClass = self::SIZES[$size] ?? self::SIZES['md'];

        // Проверяем, есть ли иконка в fill или stroke
        $isFill = isset(self::ICONS_FILL[$name]);
        $path = self::ICONS_FILL[$name] ?? self::ICONS_STROKE[$name] ?? null;

        if ($path === null) {
            return '<!-- Icon not found: ' . Html::encode($name) . ' -->';
        }

        $class = trim("$sizeClass $extraClass");

        // Для иконок с несколькими path (разделены пробелом после буквы z или Z)
        $paths = preg_split('/(?<=[zZ])\s+(?=[ML])/', $path);
        $pathsHtml = '';
        foreach ($paths as $p) {
            $pathsHtml .= '<path ' . ($isFill ? '' : 'stroke-linecap="round" stroke-linejoin="round" stroke-width="2" ') . 'd="' . $p . '"/>';
        }

        $fillAttr = $isFill ? 'currentColor' : 'none';
        $strokeAttr = $isFill ? 'none' : 'currentColor';

        $extraAttrs = '';
        foreach ($options as $key => $value) {
            if ($key !== 'class') {
                $extraAttrs .= ' ' . $key . '="' . Html::encode($value) . '"';
            }
        }

        return '<svg class="' . Html::encode($class) . '" fill="' . $fillAttr . '" stroke="' . $strokeAttr . '" viewBox="0 0 ' . ($isFill ? '20 20' : '24 24') . '"' . $extraAttrs . '>' . $pathsHtml . '</svg>';
    }

    /**
     * Получить список всех доступных иконок
     */
    public static function getAvailableIcons(): array
    {
        return array_merge(
            array_keys(self::ICONS_STROKE),
            array_keys(self::ICONS_FILL)
        );
    }

    /**
     * Проверить существование иконки
     */
    public static function exists(string $name): bool
    {
        return isset(self::ICONS_STROKE[$name]) || isset(self::ICONS_FILL[$name]);
    }

    /**
     * Рендер иконки с опциями в массиве
     *
     * @param string $name имя иконки
     * @param array $options ['class' => '...', 'size' => 'md', ...]
     * @return string
     */
    public static function svg(string $name, array $options = []): string
    {
        $size = $options['size'] ?? 'md';
        $class = $options['class'] ?? '';
        unset($options['size'], $options['class']);
        return self::show($name, $size, $class, $options);
    }
